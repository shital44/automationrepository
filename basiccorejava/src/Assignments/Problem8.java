package Assignments;

public class Problem8 {

	public static void main(String[] args) {
	//Find the greatest number in a array
       
        int[] arr = {1,3,2,4,7,3,5};
        int max=arr[0];
      
        
        for(int i=0;i<arr.length;i++)
        {
        	
        	if(arr[i]>max)
        	{
        		max=arr[i];
        		
        	
        	
        }
	}System.out.println(max);
    
	}
}
		
	

